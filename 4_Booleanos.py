#Tipos booleanos

#Comparación básica
a = 2
b = 3
print(a == b)

a = 5
b = 5
print(a == b)

#Mismo procedimiento para el caso "Distinto de "

a = 2 
b = 4
print(a != b)

a = 3
b = 3
print(a != b)

#Mismo procedimiento para "Mayor que " y "Menor que"

a = 2 
b = 4

print (a > b)

print(a < b)

#Incluso con operaciones 

print(a * 2 == b)

print(a * 3 == b)
