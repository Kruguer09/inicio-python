#Operaciones strings

#Cadena
'Hola'

#Imprimir cadena por pantalla
print('Hola')

#Asignar cadena a variable, e imprimirla por pantalla
a = 'Hola'
print(a)

#Salto de línea dentro de una cadena
a = 'Hola \nqué tal?'
print(a)

#Operaciones con cadenas

a = 'Hola'
b = 'Adios'
c = a + b
print(c)

#Multiplicación de cadenas

a = 'Hola'
c = a * 3
print(c)

#Índices

cadena = 'Hola mundo'
print(cadena[0])
print(cadena[3])
print(cadena[6])

print(cadena[-1])

#Slicing

cadena = 'Hola mundo'
print(cadena[0:3])

#Concatenar una variable con una cadena

a = 2
b = 8

print("La variable a vale", a, " y la variable b vale", b) 

